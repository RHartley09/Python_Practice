# This function will figure out if an integer is odd or even. Give it a try! #
def Is_Odd_Even(number):
    if number %2==0:
        print("Even!")
    elif number %2==1:
        print("Odd!")
    else:
         print("This isn't a number.")

# Call the function, and in the parameter, type an integer. #
Is_Odd_Even(2)

Is_Odd_Even(3)

